package operators;

public class ArithmeticsOperators {
    public static void main(String[] args) {
        /* The Arithmetics operators are used to preform Arithmetics operations like addition , 
        Subtraction , etc   */
        int a=10;
        int b=21;
        System.out.println(a+b);// addition
        System.out.println(b-a); //Subtraction
        System.out.println(a*b); //Multipliction
        System.out.println(b/a); //Division 
        System.out.println(b%a); //Modulus give reminder 

    }
    
}
