public class EscapeSequenceExample {
    public static void main(String[] args) {
        /*Scape sequence escape sequences are special character combinations used to represent characters that are otherwise difficult or impossible to type directly, or that have a special meaning in the language (like the double quote used to define a string) */

        System.out.println("Hello\nJava");

        System.out.println("Hello\tJava");

        System.out.println("\"Java\"");

        System.out.println("\\Java");
        System.out.print("This\f is my first program");
    }
    
}
