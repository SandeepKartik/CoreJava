public class NamingConventionExample {
    public static void main(String[] args) {
        /*
        Java Naming Conventions:
         1. Package names      -> lowercase
         2. Class names      -> PascalCase (Start with Capital Letter
         3. Method names       -> camelCase (Start with small letter)
         4. Variable names     -> camelCase (Start with small letter)
         5. Constant names     -> UPPER_CASE
         */
        String studentName = "Sam";
        int studentAge = 22;
        System.out.println(studentName);
        System.out.println(studentAge);
    }
    
}
