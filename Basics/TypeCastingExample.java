public class TypeCastingExample {
    public static void main(String[] args) {
        /*Type casting means change data from one data type to another 
        its maily two type  Widening Casting (Implicit) and Narrowing Casting (Explicit) */

        int num = 100;

        double d = num;

        System.out.println(d);

        double x = 25.75;

        int y = (int) x;

        System.out.println(y);
    }
    
}
